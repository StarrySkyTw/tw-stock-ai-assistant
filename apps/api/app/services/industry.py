def resolve_industry(
    symbol: str,
    name: str | None = None,
    source_industry: str | None = None,
) -> str:
    mapped = SECTOR_MAP.get(symbol.upper().strip())
    if mapped:
        return mapped

    normalized_source = normalize_source_industry(source_industry)
    if normalized_source:
        return normalized_source

    normalized_name = (name or "").upper()
    for keyword, industry in INDUSTRY_NAME_HINTS:
        if keyword.upper() in normalized_name:
            return industry
    return OTHER_INDUSTRY


def normalize_source_industry(value: str | None) -> str | None:
    text = str(value or "").strip()
    if not text:
        return None
    cleaned = text.replace("–", "-").replace("－", "-").strip(" -")
    if not cleaned:
        return None
    for keyword, industry in RAW_INDUSTRY_NORMALIZERS:
        if keyword in cleaned:
            return industry
    for suffix in ("產業類", "產業", "業類", "業", "類"):
        if cleaned.endswith(suffix) and len(cleaned) > len(suffix):
            cleaned = cleaned[: -len(suffix)].strip(" -")
            break
    if cleaned in {"其他", "其他產"}:
        return OTHER_INDUSTRY
    return cleaned
